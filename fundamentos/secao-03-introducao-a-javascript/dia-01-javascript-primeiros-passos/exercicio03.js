let valorSelecionado = -50;

if (valorSelecionado === 0) {
    console.log("O valor selecionado é ZERO: " + valorSelecionado);    
} else if (valorSelecionado >= 1){
    console.log("O valor selecionado é POSITIVO: " + valorSelecionado);    
} else {
    console.log("O valor selecionado é NEGATIVO: " + valorSelecionado);    
}